#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*dup;
	size_t	i;

	i = 0;
	while (src[i])
		i++;
	if (!(dup = (char *)malloc(sizeof(dup) * i + 1)))
		return (NULL);
	i = 0;
	while (src[i])
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
